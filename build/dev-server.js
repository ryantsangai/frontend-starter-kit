app.get('*', (req, res) => {
  return renderer.get(req.url)
    .then(html => {
      // console.log(html);
      res.send(html)
    })
    .catch(error => console.error(error))
})


const port = process.env.PORT || 8080

app.listen(port, () => {
  console.log(`server started at localhost:${port}`)
})


